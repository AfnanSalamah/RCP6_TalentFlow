export function EmailProgress({ value = 64 }) {
  const progress = Math.max(0, Math.min(100, parseInt(String(value).replace("%", ""), 10) || 64));
  return (
    <div style={{ margin: "20px 0 4px" }}>
      <table role="presentation" width="100%" cellSpacing="0" cellPadding="0">
        <tbody>
          <tr>
            <td style={{ fontSize: 13, fontWeight: 800, color: "#0F172A" }}>Profile completion</td>
            <td align="right" style={{ fontSize: 13, fontWeight: 800, color: "#2563EB" }}>{progress}%</td>
          </tr>
        </tbody>
      </table>
      <div style={{ height: 10, background: "#E2E8F0", borderRadius: 999, overflow: "hidden", marginTop: 10 }}>
        <div style={{ height: 10, width: `${progress}%`, background: "linear-gradient(135deg,#2563EB,#4F46E5)", borderRadius: 999 }} />
      </div>
    </div>
  );
}

