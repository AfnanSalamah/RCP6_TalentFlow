const TONE = {
  default: "#2563EB",
  success: "#10B981",
  warning: "#F59E0B",
  danger: "#EF4444",
};

export function EmailDataSection({ items = [] }) {
  const visible = items.filter((item) => item?.value);
  if (!visible.length) return null;

  return (
    <table role="presentation" width="100%" cellSpacing="0" cellPadding="0" style={{ margin: "20px 0", border: "1px solid #E2E8F0", borderRadius: 14, padding: "4px 16px", background: "#fff" }}>
      <tbody>
        {visible.map((item) => (
          <tr key={item.label}>
            <td style={{ padding: "12px 0", color: "#64748B", fontSize: 13, borderBottom: "1px solid #EEF2F7" }}>{item.label}</td>
            <td align="right" style={{ padding: "12px 0", color: "#0F172A", fontSize: 13, fontWeight: 800, borderBottom: "1px solid #EEF2F7" }}>{item.value}</td>
          </tr>
        ))}
      </tbody>
    </table>
  );
}

